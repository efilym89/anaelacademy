function openDetail(group, id) {
  const item = academyData[group].find((entry) => entry.id === id);
  if (!item) {
    return;
  }

  if (group === 'courses') {
    renderCourseDetail(item);
    return;
  }

  if (group === 'library') {
    renderLibraryDetail(item);
    return;
  }

  renderSimpleDetail(item);
}

